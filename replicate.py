def check_packages() -> bool:
    """Verify that required packages are importable."""
    missing: List[str] = []
    for pkg in REQUIRED_PACKAGES:
        try:
            __import__(pkg)
        except ImportError:
            missing.append(pkg)

    if missing:
        print("ERROR: Missing required packages:")
        for pkg in missing:
            print(f"  - {pkg}")
        print(f"\nInstall with:  pip install -r requirements.txt")
        return False
    return True


def check_results_dirs() -> bool:
    """Verify that expected source results subdirectories exist."""
    missing: List[str] = []
    for subdir in REQUIRED_RESULT_DIRS:
        if not (RESULTS_DIR / subdir).is_dir():
            missing.append(subdir)

    if missing:
        print("ERROR: Missing results subdirectories:")
        for d in missing:
            print(f"  - results/{d}")
        print("\nPre-computed source results are required for --verify mode.")
        print("Generated figures are intentionally not shipped; run replicate.py to create them.")
        print("Use --cas to run the full pipeline from scratch.")
        return False
    return True


DEFAULT_TIMEOUT = 7200  # 2 hours per step
LONG_TIMEOUT = 43200    # 12 hours for training-heavy steps (Phase 1)



def _mean_std(values: List[float]) -> Tuple[float, float]:
    if not values:
        return float("nan"), float("nan")
    mean = sum(values) / len(values)
    if len(values) == 1:
        return mean, 0.0
    var = sum((v - mean) ** 2 for v in values) / (len(values) - 1)
    return mean, math.sqrt(var)


def _load_json_mapes(pattern: str) -> List[Dict[str, float]]:
    rows: List[Dict[str, float]] = []
    for path in sorted(RESULTS_DIR.glob(pattern)):
        with open(path) as f:
            d = json.load(f)
        mape = d.get("mape")
        rmspe = d.get("rmspe")
        if mape is not None and mape == mape:
            rows.append({"mape": float(mape), "rmspe": float(rmspe) if rmspe is not None else float("nan")})
    return rows


def print_paper_results_summary() -> None:
    """Print the paper-facing table and figure summary."""
    archs = [
        ("gru_baseline", "GRU Baseline"),
        ("gru_attention", "GRU + Attn. (masked)"),
        ("gru_attention_unmasked", "GRU + Attn. (unmasked)"),
    ]
    lobs = [("workers_compensation", "WC"), ("private_passenger_auto", "PPA")]

    print("=" * 80)
    print("PAPER TABLE / FIGURE SUMMARY")
    print("=" * 80)
    print("\nTable 1 source: results/phase1/phase1_summary.csv\n")

    import pandas as pd
    phase1_summary = RESULTS_DIR / "phase1" / "phase1_summary.csv"
    df_phase1 = pd.read_csv(phase1_summary)

    for arch, label in archs:
        for lob, lob_short in lobs:
            subset = df_phase1[(df_phase1["arch"] == arch) & (df_phase1["lob"] == lob)]
            mapes = subset["mape"].astype(float).tolist()
            rmspes = subset["rmspe"].astype(float).tolist()
            m_mape, s_mape = _mean_std(mapes)
            m_rmspe, s_rmspe = _mean_std(rmspes)
            print(
                f"  {label:30s} {lob_short:4s} "
                f"MAPE={m_mape*100:5.2f}% ± {s_mape*100:4.2f}%  "
                f"RMSPE={m_rmspe*100:5.2f}% ± {s_rmspe*100:4.2f}%  "
                f"N={len(subset):3d}"
            )

    ppa_summary = RESULTS_DIR / "phase2" / "private_passenger_auto" / "multiseed" / "phase2_top20_summary.csv"
    if ppa_summary.exists():
        import pandas as pd
        df = pd.read_csv(ppa_summary).sort_values("mean_mape")
        print("\nNative PPA Phase 2 top configurations:")
        for _, row in df.head(5).iterrows():
            std = row.get("std_mape", 0.0)
            print(
                f"  cfg={int(row['config_idx']):3d}  "
                f"mean MAPE={row['mean_mape']*100:5.2f}%  "
                f"std={std*100:5.3f}%  "
                f"lr={row['learning_rate']:.2e}  "
                f"drop={row['dropout_rate']:.3f}  "
                f"gru={int(row['gru_units'])}"
            )

    print("\nGenerated figure/table artifacts:")
    for name in EXPECTED_FIGURES:
        path = FIGURES_DIR / name
        status = "OK" if path.exists() else "MISSING"
        print(f"  [{status:7s}] results/figures/{name}")

def run_script(
    cmd: List[str],
    label: str,
    quiet: bool = False,
    timeout: int = DEFAULT_TIMEOUT,
) -> Tuple[bool, str]:
    """Run a Python script as a subprocess.

    Returns (success, error_message).
    """
    if cmd == ["__paper_summary__"]:
        try:
            print_paper_results_summary()
            return True, ""
        except Exception as e:
            return False, str(e)

    full_cmd = [sys.executable] + cmd
    try:
        if quiet:
            result = subprocess.run(
                full_cmd,
                cwd=str(PROJECT_DIR),
                capture_output=True,
                text=True,
                timeout=timeout,
            )
            if result.returncode != 0:
                return False, result.stderr[-2000:] if result.stderr else "Non-zero exit code"
        else:
            print(f"  $ python {' '.join(cmd)}")
            result = subprocess.run(
                full_cmd,
                cwd=str(PROJECT_DIR),
                timeout=timeout,
            )
            if result.returncode != 0:
                return False, f"Exit code {result.returncode}"
        return True, ""
    except subprocess.TimeoutExpired:
        return False, f"Timed out (>{timeout // 3600}h)"
    except Exception as e:
        return False, str(e)


def run_step(
    step_num: int,
    total_steps: int,
    name: str,
    cmd: List[str],
    quiet: bool = False,
    timeout: int = DEFAULT_TIMEOUT,
) -> StepResult:
    """Run a numbered pipeline step and return its result."""
    print(f"\n[{step_num}/{total_steps}] {name}")
    print("-" * 50)

    t0 = time.time()
    success, error = run_script(cmd, name, quiet=quiet, timeout=timeout)
    elapsed = time.time() - t0

    if success:
        print(f"  Done ({format_time(elapsed)})")
    else:
        print(f"  FAILED ({format_time(elapsed)}): {error}")

    return StepResult(name, success, elapsed, error)


def count_figures() -> int:
    """Count paper-facing figure files in the figures directory."""
    if not FIGURES_DIR.is_dir():
        return 0
    return len(list(FIGURES_DIR.glob("*.pdf"))) + len(list(FIGURES_DIR.glob("*.png")))


def print_summary(results: List[StepResult], total_elapsed: float) -> None:
    """Print final summary of all steps."""
    print()
    print("=" * 60)
    print("SUMMARY")
    print("=" * 60)

    failures = [r for r in results if not r.success]
    successes = [r for r in results if r.success]

    for r in results:
        status = "OK" if r.success else "FAIL"
        print(f"  [{status:>4}] {r.name:<40} {format_time(r.elapsed):>8}")

    print("-" * 60)
    print(f"  Steps: {len(successes)}/{len(results)} succeeded")

    n_figs = count_figures()
    if n_figs > 0:
        print(f"  Figures: {n_figs} PDF/PNG files in results/figures/")

    print(f"  Total time: {format_time(total_elapsed)}")

    if failures:
        print(f"\n  FAILURES ({len(failures)}):")
        for r in failures:
            print(f"    - {r.name}: {r.message}")

    print()


