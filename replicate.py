def main() -> None:
    parser = argparse.ArgumentParser(
        description="Replicate all results from the paper.",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""\
examples:
  python replicate.py                  # verify figures from pre-computed results
  python replicate.py --cas            # full replication from CAS public data
  python replicate.py --step figures   # regenerate figures only
  python replicate.py --cas --step phase1  # run Phase 1 training only
""",
    )

    parser.add_argument(
        "--cas",
        action="store_true",
        help="Full replication: download CAS data, train all models, generate figures",
    )
    parser.add_argument(
        "--verify",
        action="store_true",
        default=True,
        help="Verify mode (default): regenerate figures from shipped results",
    )
    parser.add_argument(
        "--step",
        type=str,
        default=None,
        metavar="STEP",
        help=(
            "Run only a specific step. "
            "Verify: figures, analysis, tables. "
            "CAS: data, phase1, benchmarks, phase2, phase2-screen, phase2-validate, "
            "temporal, kuo, figures, analysis, maturity, tables"
        ),
    )
    parser.add_argument(
        "--force",
        action="store_true",
        help="Force regeneration even if output files exist (verify mode)",
    )
    parser.add_argument(
        "--quiet",
        action="store_true",
        help="Suppress subprocess output (show progress only)",
    )
    parser.add_argument(
        "--results-dir",
        type=str,
        default=None,
        metavar="DIR",
        help="Output directory for results (default: results/). "
             "Useful for running CAS replication without overwriting existing results.",
    )

    args = parser.parse_args()

    # Set results directory via env var (inherited by all subprocesses)
    if args.results_dir:
        results_path = Path(args.results_dir).resolve()
        os.environ["DEEPTRIANGLE_RESULTS"] = str(results_path)
        # Update module-level vars to match
        global RESULTS_DIR, FIGURES_DIR
        RESULTS_DIR = results_path
        FIGURES_DIR = results_path / "figures"
        print(f"Results directory: {results_path}\n")

    print_banner()
    log_hardware()

    if args.cas:
        run_cas(quiet=args.quiet, step_filter=args.step)
    else:
        run_verify(quiet=args.quiet, force=args.force, step_filter=args.step)


if __name__ == "__main__":
    main()
