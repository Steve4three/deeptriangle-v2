def run_verify(
    quiet: bool = False,
    force: bool = False,
    step_filter: Optional[str] = None,
) -> None:
    """Verify mode: regenerate figures from pre-computed results."""

    print("Mode: VERIFY (regenerate figures from shipped results)")
    print()

    if not check_packages():
        sys.exit(1)

    if not check_results_dirs():
        sys.exit(1)

    # Define analysis steps: (name, cmd, timeout)
    all_steps: List[Tuple[str, List[str], int]] = [
        ("Analyze paper figures and tables", ["analyze_results.py"], DEFAULT_TIMEOUT),
        ("Generate HP partial-dependence figure", ["analyze_hp_sensitivity.py", "--output", "results/figures"], DEFAULT_TIMEOUT),
        ("Show paper table/figure summary", ["__paper_summary__"], DEFAULT_TIMEOUT),
    ]

    # Filter to a single step if requested
    if step_filter:
        step_map: Dict[str, List[int]] = {
            "figures": [0, 1],
            "analysis": [0, 1, 2],
            "tables": [2],
        }
        indices = step_map.get(step_filter)
        if indices is None:
            valid = ", ".join(step_map.keys())
            print(f"ERROR: Unknown step '{step_filter}' for verify mode.")
            print(f"Valid steps: {valid}")
            sys.exit(1)
        all_steps = [all_steps[i] for i in indices]

    # Optionally clear existing figures (preserving hand-crafted assets)
    if force and FIGURES_DIR.is_dir():
        for f in FIGURES_DIR.glob("*.pdf"):
            if f.name not in STATIC_FIGURES:
                f.unlink()
        for f in FIGURES_DIR.glob("*.png"):
            if f.name not in STATIC_FIGURES:
                f.unlink()
        print("Cleared existing figures.\n")

    results: List[StepResult] = []
    total_steps = len(all_steps)
    t_total = time.time()

    for i, (name, cmd, step_timeout) in enumerate(all_steps, 1):
        result = run_step(i, total_steps, name, cmd, quiet=quiet, timeout=step_timeout)
        results.append(result)

    print_summary(results, time.time() - t_total)


def run_cas(
    quiet: bool = False,
    step_filter: Optional[str] = None,
) -> None:
    """CAS mode: full replication from public data."""

    print("Mode: CAS (full replication from public data)")
    print()

    # CAS data covers AY 1998-2007 (10 dev lags).
    # Following Kuo (2019): train ≤ CY 2005, val CY 2006-2007, test CY 2008.
    os.environ["DEEPTRIANGLE_TRAIN_CUTOFF"] = "2005"
    os.environ["DEEPTRIANGLE_VAL_START"] = "2006"
    os.environ["DEEPTRIANGLE_VAL_END"] = "2007"
    os.environ["DEEPTRIANGLE_TEST_CAL"] = "2008"
    os.environ["DEEPTRIANGLE_TEST_MAX_AY"] = "2007"
    os.environ["DEEPTRIANGLE_AY_MIN"] = "1998"
    print("CAS temporal splits: train ≤ CY 2005, val 2006-2007, test 2008")
    print("This will download CAS data, run all training experiments,")
    print("and regenerate all figures. Estimated time: ~5 hours on an NVIDIA GB10 GPU (DGX Spark) in our measurements.")
    print()

    if not check_packages():
        sys.exit(1)

    # CAS mode needs additional packages
    missing_extra: List[str] = []
    for pkg in CAS_EXTRA_PACKAGES:
        try:
            __import__(pkg)
        except ImportError:
            missing_extra.append(pkg)
    if missing_extra:
        print("ERROR: CAS mode requires additional packages:")
        for pkg in missing_extra:
            print(f"  - {pkg}")
        print(f"\nInstall with:  pip install {' '.join(missing_extra)}")
        sys.exit(1)

    # Define all pipeline steps: (name, cmd, timeout)
    # Training-heavy steps get LONG_TIMEOUT (12h); analysis steps use DEFAULT_TIMEOUT (2h).
    all_steps: List[Tuple[str, List[str], int]] = [
        ("Download & convert CAS data", ["convert_cas_data.py"], DEFAULT_TIMEOUT),
        ("Phase 1: Architecture comparison (300 runs)", ["run_phase1.py"], LONG_TIMEOUT),
        ("Classical benchmarks (Mack CL, ODP, BF)", ["benchmarks.py", "--no-automl"], DEFAULT_TIMEOUT),
        ("Phase 2: HP screening (100 configs)", ["run_phase2_multiseed.py", "--stage", "1"], LONG_TIMEOUT),
        ("Phase 2: Multi-seed validation (top 20)", ["run_phase2_multiseed.py", "--stage", "2"], LONG_TIMEOUT),
        ("Temporal robustness (3 windows)", ["run_temporal.py", "--archs", "gru_baseline"], LONG_TIMEOUT),
        ("Kuo comparison experiments", ["run_kuo_comparison.py"], DEFAULT_TIMEOUT),
        ("Analyze paper figures and tables", ["analyze_results.py"], DEFAULT_TIMEOUT),
        ("Generate HP partial-dependence figure", ["analyze_hp_sensitivity.py", "--output", "results/figures"], DEFAULT_TIMEOUT),
        ("Analyze maturity effects", ["analyze_maturity.py"], DEFAULT_TIMEOUT),
        ("Show paper table/figure summary", ["__paper_summary__"], DEFAULT_TIMEOUT),
    ]

    # Filter to a single step if requested
    if step_filter:
        step_map: Dict[str, List[int]] = {
            "data": [0],
            "phase1": [1],
            "benchmarks": [2],
            "phase2": [3, 4],
            "phase2-screen": [3],
            "phase2-validate": [4],
            "temporal": [5],
            "kuo": [6],
            "figures": [7, 8],
            "analysis": [7, 8, 9, 10],
            "maturity": [9],
            "tables": [10],
        }
        indices = step_map.get(step_filter)
        if indices is None:
            valid = ", ".join(step_map.keys())
            print(f"ERROR: Unknown step '{step_filter}' for --cas mode.")
            print(f"Valid steps: {valid}")
            sys.exit(1)
        all_steps = [all_steps[i] for i in indices]

    results: List[StepResult] = []
    total_steps = len(all_steps)
    t_total = time.time()

    for i, (name, cmd, step_timeout) in enumerate(all_steps, 1):
        result = run_step(i, total_steps, name, cmd, quiet=quiet, timeout=step_timeout)
        results.append(result)

    print_summary(results, time.time() - t_total)


